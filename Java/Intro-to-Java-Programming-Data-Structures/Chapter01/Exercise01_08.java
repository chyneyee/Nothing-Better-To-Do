package chapter01;

public class Exercise01_08 
{
	public static void main(String[] args) 
	{
		double radius = 6.5;
		double pi = 3.14159;
		double perimeter = 2 * radius * pi;
		double area = radius * radius * pi;
		System.out.println("Area of a circle with radius 6.5 is " +area);
		System.out.println("Perimeter of a circle with radius 6.5 is " +perimeter);
	}
}



